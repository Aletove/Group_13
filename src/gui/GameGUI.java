    public Icon selectIcon(Tile currentTile) {
        Icon currentIcon = null;
        String targetType = null;
        Random rand = new Random();
        int index = 0;
        index = rand.nextInt(3);
        if (currentTile != null) {
            targetType = currentTile.toString();
            switch (targetType) {
                case "CATS": {
                    currentIcon = catsIcons[index];
                }
                case "BOOKS": {
                    currentIcon = booksIcons[index];
                }
                case "GAMES": {
                    currentIcon = gamesIcons[index];
                }
                case "FRAMES": {
                    currentIcon = framesIcons[index];
                }
                case "TROPH": {
                    currentIcon = trophIcons[index];
                }
                case "PLANTS": {
                    currentIcon = plantsIcons[index];
                }
                case "EMPTY": {
                    currentIcon = emptyTile;
                }
                default: {
                    currentIcon = null;
                }
            }
        }

        return currentIcon;
    }

