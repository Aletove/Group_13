    private void jBtnAddPlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddPlayerActionPerformed
        // TODO add your handling code here:
        String tmp = new String();
        Iterator names;
        if (playerNames.size() < 4) {
            playerNames.add(jTFPlayerName.getText());
            jTFPlayerName.setText("");
        }
        if (playerNames.size() == 4) {
            jTFPlayerName.setEditable(false);
        }
        if (playerNames.size() > 1) {
            jBtnStartGame.setVisible(true);
        }
        names = playerNames.iterator();
        while (names.hasNext()) {
            tmp += names.next();
            tmp += ", ";
        }
        jLblAddedPlayers.setText("Added players: " + tmp);
    }//GEN-LAST:event_jBtnAddPlayerActionPerformed

